

//  Logical Operators
// Operator	Meaning	Example	Result
// &&	Logical AND	true && false	false

// true  true => true else false

let flag1 = true;
let flag2 = false;

console.log(flag1 && flag2); // false

// ||	Logical OR

// false false = false else true 

let flag3 = false;
let flag4 = false;

console.log(flag3 || flag4) // false


// !	Logical NOT	!true	false

// true => false and false => true

let flag5 = true;

console.log(!flag5) // false