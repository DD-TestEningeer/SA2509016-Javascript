

// 1. Arithmetic Operators
// Operator	Meaning	Example	Result
// +	Addition	
let num1 = 10;
let num2;
num2 = 5;

let result =  num1 + num2; // 15
console.log("The Addition of num1 and num2 : " + result);

// -	Subtraction	
result =  num1 - num2; // 5
console.log("The Subtraction of num1 and num2 : " + result);

// *	Multiplication	
result =  num1 * num2; // 50
console.log("The Multiplication of num1 and num2 : " + result);


// /	Division	
result =  num1 / num2; // 2
console.log("The Division of num1 and num2 : " + result);


// %	Modulus	 // 0 
result =  num1 % num2;
console.log("The Modulus of num1 and num2 : " + result);

// ++	Increment	let x = 1; x++	2

let num3 = 10;
console.log("The original value of num3 : " + num3); // 10

num3 = num3 +1;
console.log("The modified value of num3 : " + num3); // 10+1 = 11

num3++; // post increment
console.log("The modified value of num3 : " + num3); // 11+1 = 12

++num3; // pre-increment
console.log("The modified value of num3 : " + num3);


// --	Decrement	let y = 2; y--	1


let num4 = 10;
console.log("The original value of num4 : " + num4); // 10


num4 = num4 - 1;
console.log("The modified value of num4 : " + num4); //9

num4--; // post-decrement
console.log("The modified value of num4 : " + num4);  // 8

--num4; // pre-decrement
console.log("The modified value of num4 : " + num4); // 7