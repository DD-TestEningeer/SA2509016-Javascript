

// Switch Case Statement 

// Days in Week => 7 days > Mon to Sun

let choice = "Sun"; // current choice 


switch(choice){ 

case "Mon" : {
    console.log("Monday");
    break; // break is used to break the flow of code execution
}

case "Tue" : {
    console.log("Tuesday");
    break; // break is used to break the flow of code execution
}

case "Wed" : {
    console.log("Wednesday");
    break; // break is used to break the flow of code execution
}

case "Sun" : {
    console.log("Sunday");
    break; // break is used to break the flow of code execution
}

default : 

console.log("Choice not found !! Please try again");


}


